export function normalizeScriptError(stderr: string): string {
  const message = stderr
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .slice(-4)
    .join(" ");
  if (/faster-whisper|ModuleNotFoundError/i.test(message)) {
    return "本地识别依赖缺失，请先安装 faster-whisper：python -m pip install -U faster-whisper。";
  }
  if (/LocalEntryNotFoundError|snapshot folder|HF_HUB_OFFLINE|offline|locate the files on the Hub|Cannot find an appropriate cached snapshot/i.test(message)) {
    return "本地识别模型未预置：请先在服务器缓存或模型目录中准备 Systran/faster-whisper-base，或设置 TRANSCRIBE_OFFLINE=0 允许首次联网下载。";
  }
  if (/VAD filter requires the onnxruntime package|onnxruntime/i.test(message)) {
    return "VAD 静音过滤需要 onnxruntime。当前默认已关闭 VAD；如需开启，请安装 onnxruntime，或设置 TRANSCRIBE_VAD_FILTER=0 后重启服务。";
  }
  if (/ffmpeg|decode|Invalid data|moov atom|音视频解码/i.test(message)) {
    return "音视频解码失败，请安装/配置 ffmpeg，或换用 mp3、wav、m4a 等常见音频格式。";
  }
  return message || "本地音频识别失败，请检查文件是否包含可识别的人声。";
}

function safeDecodeURIComponent(value: string): string {
  try {
    return decodeURIComponent(value);
  } catch {
    return value;
  }
}

function cleanEnv(value: string | undefined): string | undefined {
  const cleaned = value?.trim();
  return cleaned ? cleaned : undefined;
}
