export type TranscriptionResult = {
  text: string;
  filename: string;
  duration?: number;
  language?: string;
  warnings: string[];
};

export class TranscriptionError extends Error {
  constructor(
    message: string,
    public readonly statusCode = 400
  ) {
    super(message);
  }
}

export function transcribeMaxBytes(): number {
  const raw = Number(process.env.TRANSCRIBE_MAX_BYTES);
  return Number.isFinite(raw) && raw > 0 ? raw : DEFAULT_MAX_BYTES;
}

export function transcribeTimeoutMs(): number {
  const raw = Number(process.env.TRANSCRIBE_TIMEOUT_MS);
  return Number.isFinite(raw) && raw >= 5000 ? raw : DEFAULT_TIMEOUT_MS;
}

export function supportedTranscribeExtensions(): string[] {
  return Array.from(SUPPORTED_EXTENSIONS).sort();
}

export function localTranscribeConfig() {
  return {
    python: cleanEnv(process.env.TRANSCRIBE_PYTHON) || "python",
    model: cleanEnv(process.env.TRANSCRIBE_MODEL) || DEFAULT_LOCAL_MODEL,
    language: cleanEnv(process.env.TRANSCRIBE_LANGUAGE) || "zh",
    offline: process.env.TRANSCRIBE_OFFLINE?.trim() !== "0",
    vadFilter: process.env.TRANSCRIBE_VAD_FILTER?.trim() === "1",
    worker: process.env.TRANSCRIBE_WORKER?.trim() !== "0"
  };
}

export function sanitizeUploadFilename(raw: unknown): string {
  const value = String(raw ?? "").trim();
  const decoded = safeDecodeURIComponent(value);
  const normalized = path.basename(decoded.replace(/\\/g, "/"));
  return normalized || `upload-${randomUUID()}.audio`;
}

export function validateTranscribeFilename(filename: string): string {
  const extension = path.extname(filename).toLowerCase();
  if (!SUPPORTED_EXTENSIONS.has(extension)) {
    throw new TranscriptionError(`不支持的文件格式：${extension || "无扩展名"}。支持 ${supportedTranscribeExtensions().join(", ")}。`);
  }
  return extension;
}

export function parseTranscriptionStdout(stdout: string): Pick<TranscriptionResult, "text" | "duration" | "language"> {
  const trimmed = stdout.trim();
  if (!trimmed) throw new TranscriptionError("识别完成但没有返回文字，请确认文件中包含可识别的人声。", 422);

  try {
    const payload = JSON.parse(trimmed) as { text?: unknown; duration?: unknown; language?: unknown };
    const text = normalizeChineseTranscriptionText(String(payload.text ?? "").trim());
    if (!text) throw new TranscriptionError("识别完成但没有返回文字，请确认文件中包含可识别的人声。", 422);
    const duration = typeof payload.duration === "number" && Number.isFinite(payload.duration) ? payload.duration : undefined;
    const language = typeof payload.language === "string" && payload.language.trim() ? payload.language.trim() : undefined;
    return { text, duration, language };
  } catch (error) {
    if (error instanceof TranscriptionError) throw error;
    return { text: normalizeChineseTranscriptionText(trimmed) };
  }
}

function normalizeChineseTranscriptionText(text: string): string {
  let normalized = text;
  for (const [traditional, simplified] of TRADITIONAL_TO_SIMPLIFIED_PHRASES) {
    normalized = normalized.split(traditional).join(simplified);
  }
  return normalized.replace(/[\u3400-\u9fff]/g, (char) => TRADITIONAL_TO_SIMPLIFIED_CHARS.get(char) ?? char);
}

export async function transcribeUploadedStream(input: Readable, filenameHeader: unknown, contentLengthHeader: unknown): Promise<TranscriptionResult> {
  const filename = sanitizeUploadFilename(filenameHeader);
  const extension = validateTranscribeFilename(filename);
  const expectedBytes = Number(contentLengthHeader);
  const maxBytes = transcribeMaxBytes();
  if (Number.isFinite(expectedBytes) && expectedBytes > maxBytes) {
    throw new TranscriptionError(`文件过大，请上传不超过 ${Math.round(maxBytes / 1024 / 1024)}MB 的音频或视频。`, 413);
  }

  const root = path.join(os.tmpdir(), "auto-diagram-transcribe");
  await mkdir(root, { recursive: true });
  const uploadPath = path.join(root, `${randomUUID()}${extension}`);

  try {
    await writeLimitedStream(input, uploadPath, maxBytes);
    const fileStat = await stat(uploadPath);
    if (fileStat.size <= 0) throw new TranscriptionError("上传文件为空，请选择包含音频内容的文件。");
    const parsed = await transcribeAudioFile(uploadPath);
    return { ...parsed, filename, warnings: [] };
  } finally {
    await rm(uploadPath, { force: true }).catch(() => undefined);
  }
}

async function writeLimitedStream(input: Readable, targetPath: string, maxBytes: number): Promise<void> {
  let received = 0;
  input.on("data", (chunk: Buffer) => {
    received += chunk.length;
    if (received > maxBytes) {
      input.destroy(new TranscriptionError(`文件过大，请上传不超过 ${Math.round(maxBytes / 1024 / 1024)}MB 的音频或视频。`, 413));
    }
  });
  await pipeline(input, createWriteStream(targetPath));
}

export async function transcribeAudioFile(inputPath: string): Promise<Pick<TranscriptionResult, "text" | "duration" | "language">> {
  if (!localTranscribeConfig().worker) return runTranscriptionScript(inputPath);
  try {
    return await transcribeWithWorker(inputPath);
  } catch {
    return runTranscriptionScript(inputPath);
  }
}

async function runTranscriptionScript(inputPath: string): Promise<Pick<TranscriptionResult, "text" | "duration" | "language">> {
  const scriptPath = await resolveTranscriptionScriptPath();
  const config = localTranscribeConfig();
  const timeoutMs = transcribeTimeoutMs();

  const child = spawn(config.python, [scriptPath, inputPath, "--model", config.model, "--language", config.language, config.vadFilter ? "--vad-filter" : "--no-vad-filter"], {
    cwd: process.cwd(),
    env: {
      ...process.env,
      PYTHONIOENCODING: "utf-8",
      ...(config.offline ? { HF_HUB_OFFLINE: "1" } : {})
    },
    windowsHide: true,
    stdio: ["ignore", "pipe", "pipe"]
  });

  let stdout = "";
  let stderr = "";
  child.stdout.setEncoding("utf8");
  child.stderr.setEncoding("utf8");
  child.stdout.on("data", (chunk) => {
    stdout += chunk;
  });
  child.stderr.on("data", (chunk) => {
    stderr += chunk;
  });

  const exitCode = await new Promise<number | null>((resolve, reject) => {
    const timer = setTimeout(() => {
      child.kill();
      reject(new TranscriptionError(`音频识别超过 ${Math.round(timeoutMs / 1000)} 秒，请换用更短的文件或调大 TRANSCRIBE_TIMEOUT_MS。`, 504));
    }, timeoutMs);
    child.on("error", (error) => {
      clearTimeout(timer);
      reject(new TranscriptionError(`无法启动本地识别脚本：${error.message}`, 500));
    });
    child.on("close", (code) => {
      clearTimeout(timer);
      resolve(code);
    });
  });

  if (exitCode !== 0) {
    throw new TranscriptionError(normalizeScriptError(stderr), exitCode === 2 ? 500 : 422);
  }

  return parseTranscriptionStdout(stdout);
}

type WorkerRequest = {
  id: string;
  inputPath: string;
  resolve: (value: Pick<TranscriptionResult, "text" | "duration" | "language">) => void;
  reject: (error: Error) => void;
  timer: ReturnType<typeof setTimeout>;
};

let transcriptionWorker: ChildProcessWithoutNullStreams | null = null;
let transcriptionWorkerReady: Promise<void> | null = null;
let transcriptionWorkerBuffer = "";
let transcriptionWorkerStderr = "";
const transcriptionWorkerRequests = new Map<string, WorkerRequest>();

async function transcribeWithWorker(inputPath: string): Promise<Pick<TranscriptionResult, "text" | "duration" | "language">> {
  await ensureTranscriptionWorker();
  const worker = transcriptionWorker;
  if (!worker?.stdin.writable) throw new TranscriptionError("本地识别常驻进程不可用。", 500);

  return new Promise((resolve, reject) => {
    const id = randomUUID();
    const timeoutMs = transcribeTimeoutMs();
    const timer = setTimeout(() => {
      transcriptionWorkerRequests.delete(id);
      restartTranscriptionWorker();
      reject(new TranscriptionError(`音频识别超过 ${Math.round(timeoutMs / 1000)} 秒，请换用更短的文件或调大 TRANSCRIBE_TIMEOUT_MS。`, 504));
    }, timeoutMs);
    transcriptionWorkerRequests.set(id, { id, inputPath, resolve, reject, timer });
    worker.stdin.write(`${JSON.stringify({ id, type: "transcribe", input: inputPath })}\n`, "utf8", (error) => {
      if (!error) return;
      clearTimeout(timer);
      transcriptionWorkerRequests.delete(id);
      reject(new TranscriptionError(`无法写入本地识别常驻进程：${error.message}`, 500));
    });
  });
}

async function ensureTranscriptionWorker(): Promise<void> {
  if (transcriptionWorker?.stdin.writable && transcriptionWorkerReady) return transcriptionWorkerReady;

  const scriptPath = await resolveTranscriptionWorkerPath();
  const config = localTranscribeConfig();
  transcriptionWorkerBuffer = "";
  transcriptionWorkerStderr = "";
  const child = spawn(config.python, [scriptPath, "--model", config.model, "--language", config.language, config.vadFilter ? "--vad-filter" : "--no-vad-filter"], {
    cwd: process.cwd(),
    env: {
      ...process.env,
      PYTHONIOENCODING: "utf-8",
      ...(config.offline ? { HF_HUB_OFFLINE: "1" } : {})
    },
    windowsHide: true,
    stdio: ["pipe", "pipe", "pipe"]
  });
  transcriptionWorker = child;
  child.stdout.setEncoding("utf8");
  child.stderr.setEncoding("utf8");
  child.stdout.on("data", handleTranscriptionWorkerStdout);
  child.stderr.on("data", (chunk) => {
    transcriptionWorkerStderr = `${transcriptionWorkerStderr}${chunk}`.slice(-8000);
  });
  child.on("error", (error) => {
    rejectAllWorkerRequests(new TranscriptionError(`无法启动本地识别常驻进程：${error.message}`, 500));
    transcriptionWorker = null;
    transcriptionWorkerReady = null;
  });
  child.on("close", () => {
    rejectAllWorkerRequests(new TranscriptionError(normalizeScriptError(transcriptionWorkerStderr), 500));
    transcriptionWorker = null;
    transcriptionWorkerReady = null;
  });

  transcriptionWorkerReady = new Promise((resolve, reject) => {
    const timeoutMs = Math.min(transcribeTimeoutMs(), 120000);
    const timer = setTimeout(() => {
      restartTranscriptionWorker();
      reject(new TranscriptionError(`本地识别常驻进程启动超过 ${Math.round(timeoutMs / 1000)} 秒。`, 504));
    }, timeoutMs);
    const readyId = randomUUID();
    transcriptionWorkerRequests.set(readyId, {
      id: readyId,
      inputPath: "",
      timer,
      resolve: () => {
        clearTimeout(timer);
        resolve();
      },
      reject: (error) => {
        clearTimeout(timer);
        reject(error);
      }
    });
  });

  return transcriptionWorkerReady;
}

function handleTranscriptionWorkerStdout(chunk: string) {
  transcriptionWorkerBuffer += chunk;
  const lines = transcriptionWorkerBuffer.split(/\r?\n/);
  transcriptionWorkerBuffer = lines.pop() ?? "";
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    handleTranscriptionWorkerMessage(trimmed);
  }
}

function handleTranscriptionWorkerMessage(line: string) {
  let message: { type?: unknown; id?: unknown; text?: unknown; duration?: unknown; language?: unknown; error?: unknown };
  try {
    message = JSON.parse(line);
  } catch {
    return;
  }

  if (message.type === "ready") {
    for (const request of transcriptionWorkerRequests.values()) {
      if (request.inputPath === "") {
        transcriptionWorkerRequests.delete(request.id);
        request.resolve({ text: "", duration: undefined, language: undefined });
        break;
      }
    }
    return;
  }

  const id = String(message.id ?? "");
  const request = transcriptionWorkerRequests.get(id);
  if (!request) return;
  transcriptionWorkerRequests.delete(id);
  clearTimeout(request.timer);

  if (message.type === "error") {
    request.reject(new TranscriptionError(normalizeScriptError(String(message.error ?? "")), 422));
    return;
  }

  request.resolve(
    parseTranscriptionStdout(
      JSON.stringify({
        text: message.text,
        duration: message.duration,
        language: message.language
      })
    )
  );
}

function restartTranscriptionWorker() {
  const worker = transcriptionWorker;
  transcriptionWorker = null;
  transcriptionWorkerReady = null;
  if (worker && !worker.killed) {
    worker.kill();
  }
}

function rejectAllWorkerRequests(error: Error) {
  for (const request of transcriptionWorkerRequests.values()) {
    clearTimeout(request.timer);
    request.reject(error);
  }
  transcriptionWorkerRequests.clear();
}

